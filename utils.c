/* ======================== WHITESPACE ======================== */
int     skip_spaces(const char *text, int i)
{
    if (!text) return i;
    while (text[i] && text[i] == ' ')
        i++;
    return i;
}
int ft_isspace(int c)
{
    return (c == ' ' || c == '\t' || c == '\n'
         || c == '\v' || c == '\f' || c == '\r');
}
